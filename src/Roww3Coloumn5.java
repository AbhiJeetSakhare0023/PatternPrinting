public class Roww3Coloumn5 {

        public static void main(String[] args) {
            for(int i=1;i<=3;i++) // outer loop for row purpose
            {
                for(int j=1;j<=5;j++) // inner loop for column purpose
                {
                    System.out.print(" * ");
                }
                System.out.println(" ");
            }
        }


}
